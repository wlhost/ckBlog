<script>
    layui.config({
        base: "{{ URL::asset('backend/') }}/" //静态资源所在路径
    }).extend({
        index: 'lib/index' //主入口模块
    }).use(['index', 'form'], function () {
        var $ = layui.$
            , form = layui.form

        //提交
        form.on('submit(LAY-user-login-submit)', function (obj) {

            $.ajax({
                type: "POST",
                url:"{{ url('admin/login') }}",
                data:obj.field,
                dataType:'json',
                success: function(res) {
                    console.log(res);
                    layer.msg('登入成功', {
                        offset: '15px'
                        , icon: 1
                        , time: 1000
                    }, function () {
                        location.href = "{{ url('admin') }}"; //后台主页
                    });
                },
                error : function (msg) {
                    if (msg.status == 422) {
                        var json=JSON.parse(msg.responseText);
                        json = json.errors;
                        for ( var item in json) {
                            for ( var i = 0; i < json[item].length; i++) {
                                layer.msg(JSON.parse(json[item][i]).msg);
                                return ; //遇到验证错误，就退出
                            }
                        }

                    } else {
                        alert('服务器连接失败');
                        return ;
                    }
                }
            });

        });


    });
</script>
